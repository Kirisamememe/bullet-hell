const CHAR_W = 5;
const CHAR_H = 7;

export function drawPixelText(
  ctx: CanvasRenderingContext2D,
  text: string,
  x: number,
  y: number,
  color = '#ffffff',
  scale = 1
): number {
  let ox = x;
  for (const ch of text.toUpperCase()) {
    const glyph = FONT[ch];
    if (glyph) {
      for (let row = 0; row < CHAR_H; row++) {
        for (let col = 0; col < CHAR_W; col++) {
          if (glyph[row] & (1 << (CHAR_W - 1 - col))) {
            ctx.fillStyle = color;
            ctx.fillRect(
              ox + col * scale,
              y + row * scale,
              scale,
              scale
            );
          }
        }
      }
      ox += (CHAR_W + 1) * scale;
    } else {
      ox += (CHAR_W + 1) * scale;
    }
  }
  return ox - x;
}

export function drawPixelChar(
  ctx: CanvasRenderingContext2D,
  char: string,
  x: number,
  y: number,
  color = '#ffffff',
  scale = 1
): void {
  drawPixelText(ctx, char, x, y, color, scale);
}
